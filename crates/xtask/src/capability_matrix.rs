const DEFAULT_OUT_PATH: &str = "docs/specs/unified-agent-api/capability-matrix.md";
const CURRENT_MANIFEST_FILENAME: &str = "current.json";

const CAPABILITY_MCP_LIST_V1: &str = "agent_api.tools.mcp.list.v1";
const CAPABILITY_MCP_GET_V1: &str = "agent_api.tools.mcp.get.v1";
const CAPABILITY_MCP_ADD_V1: &str = "agent_api.tools.mcp.add.v1";
const CAPABILITY_MCP_REMOVE_V1: &str = "agent_api.tools.mcp.remove.v1";

#[derive(Debug, Parser)]
pub struct Args {
    /// Write the generated markdown to this path. Defaults to the workspace-root capability matrix path when omitted.
    #[arg(long)]
    pub out: Option<PathBuf>,
}

pub fn run(args: Args) -> Result<(), String> {
    let inventory = collect_builtin_backend_inventory()?;
    let markdown = render_matrix(&inventory.backends, &inventory.canonical_target_header);
    let out = resolve_output_path(args.out.as_deref());
    write_file(&out, &markdown)?;
    Ok(())
}

pub(crate) fn collect_builtin_backend_capabilities(
) -> Result<BTreeMap<String, AgentWrapperCapabilities>, String> {
    collect_builtin_backend_inventory().map(|inventory| inventory.backends)
}

fn write_file(path: &Path, contents: &str) -> Result<(), String> {
    if let Some(parent) = path.parent() {
        fs::create_dir_all(parent).map_err(|err| format!("create_dir_all({path:?}): {err}"))?;
    }
    fs::write(path, contents).map_err(|err| format!("write({path:?}): {err}"))?;
    Ok(())
}

fn render_matrix(
    backends: &BTreeMap<String, AgentWrapperCapabilities>,
    canonical_target_header: &str,
) -> String {
    let backend_ids: Vec<String> = backends.keys().cloned().collect();

    let mut all_caps = BTreeSet::<String>::new();
    for caps in backends.values() {
        all_caps.extend(caps.ids.iter().cloned());
    }

    let mut buckets = BTreeMap::<String, Vec<String>>::new();
    for cap in all_caps {
        let bucket = bucket_for(&cap);
        buckets.entry(bucket).or_default().push(cap);
    }

    for caps in buckets.values_mut() {
        caps.sort();
        caps.dedup();
    }

    let mut out = String::new();
    out.push_str("# Capability matrix — Unified Agent API\n\n");
    out.push_str("Status: Approved  \n");
    out.push_str("Approved (UTC): 2026-02-21\n\n");
    out.push_str("This file is generated by `cargo run -p xtask -- capability-matrix`.\n");
    out.push_str(canonical_target_header);
    out.push_str("This inventory documents backend capability advertising, not support or promotion status.\n");
    out.push_str("Do not edit by hand.\n\n");

    let bucket_order = bucket_order(&backend_ids);
    for bucket in bucket_order {
        let Some(cap_ids) = buckets.get(&bucket) else {
            continue;
        };
        if cap_ids.is_empty() {
            continue;
        }

        out.push_str(&format!("## `{bucket}`\n\n"));
        out.push_str("| capability id |");
        for backend_id in backend_ids.iter() {
            out.push_str(&format!(" `{backend_id}` |"));
        }
        out.push('\n');

        out.push_str("|---|");
        for _ in backend_ids.iter() {
            out.push_str("---|");
        }
        out.push('\n');

        for cap_id in cap_ids.iter() {
            out.push_str(&format!("| `{cap_id}` |"));
            for backend_id in backend_ids.iter() {
                let supported = backends
                    .get(backend_id)
                    .is_some_and(|caps| caps.contains(cap_id));
                out.push_str(if supported { " ✅ |" } else { " — |" });
            }
            out.push('\n');
        }
        out.push('\n');
    }

    out
}

fn bucket_for(capability_id: &str) -> String {
    if capability_id == "agent_api.run" || capability_id == "agent_api.events" {
        return "agent_api.core".to_string();
    }

    if capability_id == "agent_api.events" || capability_id.starts_with("agent_api.events.") {
        return "agent_api.events".to_string();
    }

    if capability_id.starts_with("agent_api.exec.") {
        return "agent_api.exec".to_string();
    }

    if capability_id.starts_with("agent_api.session.") {
        return "agent_api.session".to_string();
    }

    for prefix in [
        "agent_api.tools.",
        "agent_api.artifacts.",
        "agent_api.control.",
        "agent_api.config.",
    ] {
        if capability_id.starts_with(prefix) {
            let bucket = prefix.trim_end_matches('.');
            return bucket.to_string();
        }
    }

    if capability_id.starts_with("agent_api.") {
        return "agent_api.other".to_string();
    }

    if let Some(agent_kind) = capability_id.strip_prefix("backend.") {
        let agent_kind = agent_kind
            .split_once('.')
            .map(|(head, _)| head)
            .unwrap_or(agent_kind);
        return format!("backend.{agent_kind}");
    }

    "other".to_string()
}

fn bucket_order(backend_ids: &[String]) -> Vec<String> {
    let mut out = Vec::new();
    out.extend([
        "agent_api.core".to_string(),
        "agent_api.events".to_string(),
        "agent_api.exec".to_string(),
        "agent_api.session".to_string(),
        "agent_api.tools".to_string(),
        "agent_api.artifacts".to_string(),
        "agent_api.control".to_string(),
        "agent_api.config".to_string(),
        "agent_api.other".to_string(),
    ]);

    let mut backend_buckets: Vec<String> = backend_ids
        .iter()
        .map(|id| format!("backend.{id}"))
        .collect();
    backend_buckets.sort();
    backend_buckets.dedup();
    out.extend(backend_buckets);

    out.push("other".to_string());
    out
}

fn collect_builtin_backend_inventory() -> Result<BuiltinBackendInventory, String> {
    let registry = load_agent_registry()?;
    let enrolled_entries: Vec<&AgentRegistryEntry> = registry.capability_matrix_entries().collect();

    let mut backends = BTreeMap::<String, AgentWrapperCapabilities>::new();
    for entry in enrolled_entries.iter().copied() {
        let (backend_kind, mut capabilities) = runtime_backend_capabilities(&entry.agent_id)?;
        if backend_kind != entry.agent_id {
            return Err(format!(
                "capability-matrix registry entry `{}` does not match runtime backend kind `{backend_kind}`",
                entry.agent_id
            ));
        }
        apply_registry_mcp_projection(&mut capabilities, entry)?;
        backends.insert(backend_kind, capabilities);
    }

    Ok(BuiltinBackendInventory {
        backends,
        canonical_target_header: render_canonical_target_header(&enrolled_entries),
    })
}

fn apply_manifest_mcp_projection(
    capabilities: &mut AgentWrapperCapabilities,
    registry_entry: &AgentRegistryEntry,
    manifest: &UnionManifest,
    target: &str,
) {
    for capability_id in [
        CAPABILITY_MCP_LIST_V1,
        CAPABILITY_MCP_GET_V1,
        CAPABILITY_MCP_ADD_V1,
        CAPABILITY_MCP_REMOVE_V1,
    ] {
        let declaration = declared_mcp_projection(registry_entry, capability_id);
        let available_on_canonical_target = declaration.as_ref().is_some_and(|declaration| {
            declaration.supports_target(target)
                && command_available_on_target(manifest, mcp_command_path(capability_id), target)
        });
        let advertise_when_available =
            declaration.is_some_and(|declaration| declaration.advertise_when_available);

        sync_capability_from_manifest(
            capabilities,
            capability_id,
            available_on_canonical_target,
            advertise_when_available,
        );
    }
}

fn sync_capability_from_manifest(
    capabilities: &mut AgentWrapperCapabilities,
    capability_id: &str,
    available_on_canonical_target: bool,
    advertise_when_available: bool,
) {
    if available_on_canonical_target {
        if advertise_when_available {
            capabilities.ids.insert(capability_id.to_string());
        }
        return;
    }

    capabilities.ids.remove(capability_id);
}

fn command_available_on_target(manifest: &UnionManifest, path: &[&str], target: &str) -> bool {
    manifest
        .commands
        .iter()
        .find(|command| {
            command
                .path
                .iter()
                .map(String::as_str)
                .eq(path.iter().copied())
        })
        .is_some_and(|command| command.available_on.iter().any(|item| item == target))
}

fn resolve_output_path(explicit: Option<&Path>) -> PathBuf {
    match explicit {
        Some(path) => path.to_path_buf(),
        None => resolve_workspace_path(Path::new(DEFAULT_OUT_PATH)),
    }
}

fn resolve_workspace_path(path: &Path) -> PathBuf {
    workspace_root().join(path)
}

fn workspace_root() -> PathBuf {
    PathBuf::from(env!("CARGO_MANIFEST_DIR"))
        .parent()
        .and_then(|p| p.parent())
        .expect("CARGO_MANIFEST_DIR has crates/xtask parent structure")
        .to_path_buf()
}

fn read_union_manifest(path: &Path) -> Result<UnionManifest, String> {
    let text = fs::read_to_string(path).map_err(|err| format!("read({path:?}): {err}"))?;
    serde_json::from_str(&text).map_err(|err| format!("parse({path:?}): {err}"))
}

fn load_agent_registry() -> Result<AgentRegistry, String> {
    AgentRegistry::load(&workspace_root()).map_err(|err| err.to_string())
}

#[cfg(test)]
fn capability_matrix_backend_ids(registry: &AgentRegistry) -> Vec<String> {
    registry
        .capability_matrix_entries()
        .map(|entry| entry.agent_id.clone())
        .collect()
}

fn runtime_backend_capabilities(
    agent_id: &str,
) -> Result<(String, AgentWrapperCapabilities), String> {
    match agent_id {
        "codex" => {
            let backend = CodexBackend::new(CodexBackendConfig::default());
            Ok((backend.kind().as_str().to_string(), backend.capabilities()))
        }
        "claude_code" => {
            let backend = ClaudeCodeBackend::new(ClaudeCodeBackendConfig::default());
            Ok((backend.kind().as_str().to_string(), backend.capabilities()))
        }
        "opencode" => {
            let backend = OpencodeBackend::new(OpencodeBackendConfig::default());
            Ok((backend.kind().as_str().to_string(), backend.capabilities()))
        }
        other => Err(format!(
            "capability-matrix registry enrolled unsupported backend `{other}`"
        )),
    }
}

fn apply_registry_mcp_projection(
    capabilities: &mut AgentWrapperCapabilities,
    registry_entry: &AgentRegistryEntry,
) -> Result<(), String> {
    let BackendProjectionPolicy::CanonicalTarget(target) =
        backend_projection_policy(registry_entry)
    else {
        return Ok(());
    };

    let manifest_path = resolve_workspace_path(
        &PathBuf::from(&registry_entry.manifest_root).join(CURRENT_MANIFEST_FILENAME),
    );
    let manifest = read_union_manifest(&manifest_path)?;
    apply_manifest_mcp_projection(capabilities, registry_entry, &manifest, &target);
    Ok(())
}

fn render_canonical_target_header(entries: &[&AgentRegistryEntry]) -> String {
    let mut canonical_targets = Vec::<String>::new();
    let mut default_backends = Vec::<String>::new();

    for entry in entries.iter().copied() {
        match backend_projection_policy(entry) {
            BackendProjectionPolicy::CanonicalTarget(target) => {
                canonical_targets.push(format!("`{}={target}`", entry.agent_id))
            }
            BackendProjectionPolicy::DefaultBuiltInConfig => {
                default_backends.push(format!("`{}`", entry.agent_id))
            }
        }
    }

    let mut out = String::from("Canonical target profile: ");
    if !canonical_targets.is_empty() {
        out.push_str(&canonical_targets.join(", "));
    }
    if !canonical_targets.is_empty() && !default_backends.is_empty() {
        out.push_str("; ");
    }
    if !default_backends.is_empty() {
        out.push_str(&default_backends.join(", "));
        out.push(' ');
        out.push_str(if default_backends.len() == 1 {
            "uses"
        } else {
            "use"
        });
        out.push_str(" the default built-in backend config");
    }
    out.push_str(".\n");
    out
}

fn backend_projection_policy(entry: &AgentRegistryEntry) -> BackendProjectionPolicy {
    if known_mcp_capability_ids()
        .iter()
        .any(|capability_id| declared_mcp_projection(entry, capability_id).is_some())
    {
        return BackendProjectionPolicy::CanonicalTarget(
            primary_canonical_target(entry).to_string(),
        );
    }

    BackendProjectionPolicy::DefaultBuiltInConfig
}

fn primary_canonical_target(entry: &AgentRegistryEntry) -> &str {
    entry
        .canonical_targets
        .first()
        .map(String::as_str)
        .unwrap_or("")
}

fn known_mcp_capability_ids() -> [&'static str; 4] {
    [
        CAPABILITY_MCP_LIST_V1,
        CAPABILITY_MCP_GET_V1,
        CAPABILITY_MCP_ADD_V1,
        CAPABILITY_MCP_REMOVE_V1,
    ]
}

fn mcp_command_path(capability_id: &str) -> &'static [&'static str] {
    match capability_id {
        CAPABILITY_MCP_LIST_V1 => &["mcp", "list"],
        CAPABILITY_MCP_GET_V1 => &["mcp", "get"],
        CAPABILITY_MCP_ADD_V1 => &["mcp", "add"],
        CAPABILITY_MCP_REMOVE_V1 => &["mcp", "remove"],
        _ => &[],
    }
}

fn declared_mcp_projection<'a>(
    registry_entry: &'a AgentRegistryEntry,
    capability_id: &str,
) -> Option<DeclaredMcpProjection<'a>> {
    if let Some(target_gated) = registry_entry
        .capability_declaration
        .target_gated
        .iter()
        .find(|declaration| declaration.capability_id == capability_id)
    {
        return Some(DeclaredMcpProjection {
            targets: Some(target_gated.targets.as_slice()),
            advertise_when_available: true,
        });
    }

    registry_entry
        .capability_declaration
        .config_gated
        .iter()
        .find(|declaration| declaration.capability_id == capability_id)
        .map(|config_gated| DeclaredMcpProjection {
            targets: config_gated.targets.as_deref(),
            advertise_when_available: false,
        })
}

#[derive(Debug, Deserialize)]
struct UnionManifest {
    commands: Vec<UnionCommand>,
}

#[derive(Debug, Deserialize)]
struct UnionCommand {
    path: Vec<String>,
    available_on: Vec<String>,
}

#[derive(Debug)]
struct BuiltinBackendInventory {
    backends: BTreeMap<String, AgentWrapperCapabilities>,
    canonical_target_header: String,
}

#[derive(Debug, Clone, PartialEq, Eq)]
enum BackendProjectionPolicy {
    CanonicalTarget(String),
    DefaultBuiltInConfig,
}

#[derive(Debug, Clone, Copy)]
struct DeclaredMcpProjection<'a> {
    targets: Option<&'a [String]>,
    advertise_when_available: bool,
}

impl DeclaredMcpProjection<'_> {
    fn supports_target(&self, target: &str) -> bool {
        match self.targets {
            Some(targets) => targets.iter().any(|candidate| candidate == target),
            None => true,
        }
    }
}

#[cfg(test)]
mod tests {
    use super::*;

    const SEEDED_REGISTRY: &str = include_str!("../data/agent_registry.toml");

    fn manifest_with_commands(commands: &[(&[&str], &[&str])]) -> UnionManifest {
        UnionManifest {
            commands: commands
                .iter()
                .map(|(path, available_on)| UnionCommand {
                    path: path.iter().map(|segment| (*segment).to_string()).collect(),
                    available_on: available_on
                        .iter()
                        .map(|target| (*target).to_string())
                        .collect(),
                })
                .collect(),
        }
    }

    fn seeded_registry() -> AgentRegistry {
        AgentRegistry::parse(SEEDED_REGISTRY).expect("parse seeded registry")
    }

    #[test]
    fn command_available_on_target_matches_exact_path_and_target() {
        let manifest = manifest_with_commands(&[
            (&["mcp", "list"], &["linux-x64"]),
            (&["mcp", "get"], &["win32-x64"]),
        ]);

        assert!(command_available_on_target(
            &manifest,
            &["mcp", "list"],
            "linux-x64"
        ));
        assert!(!command_available_on_target(
            &manifest,
            &["mcp"],
            "linux-x64"
        ));
        assert!(!command_available_on_target(
            &manifest,
            &["mcp", "get"],
            "linux-x64"
        ));
    }

    #[test]
    fn manifest_projection_adds_read_caps_and_clears_unavailable_caps() {
        let manifest = manifest_with_commands(&[
            (&["mcp", "list"], &["x86_64-unknown-linux-musl"]),
            (&["mcp", "get"], &["x86_64-unknown-linux-musl"]),
        ]);
        let registry = seeded_registry();
        let codex = registry.find("codex").expect("seeded codex entry");
        let mut capabilities = AgentWrapperCapabilities {
            ids: [
                CAPABILITY_MCP_ADD_V1.to_string(),
                CAPABILITY_MCP_REMOVE_V1.to_string(),
            ]
            .into_iter()
            .collect(),
        };

        apply_manifest_mcp_projection(
            &mut capabilities,
            codex,
            &manifest,
            "x86_64-unknown-linux-musl",
        );

        assert!(capabilities.contains(CAPABILITY_MCP_LIST_V1));
        assert!(capabilities.contains(CAPABILITY_MCP_GET_V1));
        assert!(!capabilities.contains(CAPABILITY_MCP_ADD_V1));
        assert!(!capabilities.contains(CAPABILITY_MCP_REMOVE_V1));
    }

    #[test]
    fn resolve_output_path_defaults_to_workspace_root() {
        assert_eq!(
            resolve_output_path(None),
            workspace_root().join(DEFAULT_OUT_PATH)
        );
    }

    #[test]
    fn resolve_output_path_preserves_absolute_path() {
        let absolute = std::env::temp_dir().join("capability-matrix-absolute.md");
        assert_eq!(resolve_output_path(Some(absolute.as_path())), absolute);
    }

    #[test]
    fn resolve_output_path_preserves_explicit_relative_path() {
        let relative = Path::new("tmp/capability-matrix.md");
        assert_eq!(resolve_output_path(Some(relative)), relative);
    }

    #[test]
    fn collect_builtin_backend_capabilities_includes_opencode() {
        let backends = collect_builtin_backend_capabilities().expect("collect backends");

        assert!(backends.contains_key("opencode"));
        assert_eq!(
            backends["opencode"].ids,
            [
                "agent_api.run".to_string(),
                "agent_api.events".to_string(),
                "agent_api.events.live".to_string(),
                "agent_api.config.model.v1".to_string(),
                "agent_api.session.resume.v1".to_string(),
                "agent_api.session.fork.v1".to_string(),
            ]
            .into_iter()
            .collect()
        );
    }

    #[test]
    fn capability_matrix_backend_ids_follow_registry_enrollment_order() {
        let registry = seeded_registry();

        assert_eq!(
            capability_matrix_backend_ids(&registry),
            vec![
                "codex".to_string(),
                "claude_code".to_string(),
                "opencode".to_string()
            ]
        );
    }

    #[test]
    fn runtime_backend_kinds_match_seeded_registry_agent_ids() {
        let registry = seeded_registry();

        for entry in registry.capability_matrix_entries() {
            let (backend_kind, _) =
                runtime_backend_capabilities(&entry.agent_id).expect("runtime backend");
            assert_eq!(backend_kind, entry.agent_id);
        }
    }

    #[test]
    fn canonical_target_header_uses_registry_primary_targets() {
        let registry = seeded_registry();
        let entries: Vec<&AgentRegistryEntry> = registry.capability_matrix_entries().collect();

        assert_eq!(
            render_canonical_target_header(&entries),
            "Canonical target profile: `codex=x86_64-unknown-linux-musl`, `claude_code=linux-x64`; `opencode` uses the default built-in backend config.\n"
        );
    }

    #[test]
    fn registry_driven_mcp_projection_uses_primary_canonical_target() {
        let registry = seeded_registry();
        let claude = registry
            .find("claude_code")
            .expect("seeded claude_code entry");
        let manifest = manifest_with_commands(&[
            (&["mcp", "list"], &["linux-x64", "win32-x64"]),
            (&["mcp", "get"], &["linux-x64", "win32-x64"]),
            (&["mcp", "add"], &["linux-x64", "win32-x64"]),
            (&["mcp", "remove"], &["linux-x64", "win32-x64"]),
        ]);
        let mut capabilities = AgentWrapperCapabilities {
            ids: [
                CAPABILITY_MCP_LIST_V1.to_string(),
                CAPABILITY_MCP_GET_V1.to_string(),
                CAPABILITY_MCP_ADD_V1.to_string(),
                CAPABILITY_MCP_REMOVE_V1.to_string(),
            ]
            .into_iter()
            .collect(),
        };

        apply_manifest_mcp_projection(
            &mut capabilities,
            claude,
            &manifest,
            primary_canonical_target(claude),
        );

        assert!(capabilities.contains(CAPABILITY_MCP_LIST_V1));
        assert!(!capabilities.contains(CAPABILITY_MCP_GET_V1));
        assert!(!capabilities.contains(CAPABILITY_MCP_ADD_V1));
        assert!(!capabilities.contains(CAPABILITY_MCP_REMOVE_V1));
    }
}
